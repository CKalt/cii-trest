package com.cii.model.structurednegotiation;

public class StatusInfo {
    private String statusMessage;
    private String planHistoryMessage;

    public String getStatusMessage() {
        return statusMessage;
    }

    public void setStatusMessage(String statusMessage) {
        this.statusMessage = statusMessage;
    }

    public String getPlanHistoryMessage() {
        return planHistoryMessage;
    }

    public void setPlanHistoryMessage(String planHistoryMessage) {
        this.planHistoryMessage = planHistoryMessage;
    }
}
